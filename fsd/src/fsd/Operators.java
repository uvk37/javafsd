package fsd;

public class Operators {

	public static void main(String[] args) {
		int result = 1 + 2;
		System.out.println("1 + 2 = " + result);
		
		result = result - 1;
		
		result = result * 10;
		
		result = result / 5;
		
		System.out.println("Result =" + result);
		
		result = result % 2;
		System.out.println(result);
		
		result++;
		++result;
		
		System.out.println(result);
		
		result += 2;
		
		System.out.println(result);
		
		result *= 3;
		
		System.out.println(result);
		
		
	}
	
}
